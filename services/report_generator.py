from reportlab.platypus import (
    SimpleDocTemplate,
    Paragraph,
    Spacer,
    PageBreak
)

from reportlab.lib.styles import getSampleStyleSheet


def generate_report(
    student_name,
    prediction,
    confidence,
    risk_score,
    risk_level,
    strong_areas,
    weak_areas,
    suggestions,
    careers
):

    filename = (
    f"reports/"
    f"{student_name}_{prediction}.pdf"
)

    doc = SimpleDocTemplate(filename)

    styles = getSampleStyleSheet()

    content = []

    # Title
    content.append(
        Paragraph(
            "AI-Powered Student Performance & Career Guidance Report",
            styles["Title"]
        )
    )

    content.append(Spacer(1, 20))

    # Student Information
    content.append(
        Paragraph(
            "<b>Student Information</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            f"Student Name: {student_name}",
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 10))

    # Executive Summary
    content.append(
        Paragraph(
            "<b>Executive Summary</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            f"""
            The machine learning model predicts that the student
            will achieve <b>{prediction.upper()}</b> performance
            with a confidence score of <b>{confidence}%</b>.
            """,
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 10))

    # Prediction
    content.append(
        Paragraph(
            "<b>Prediction Results</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            f"Predicted Performance : {prediction.upper()}",
            styles["BodyText"]
        )
    )

    content.append(
        Paragraph(
            f"Confidence Score : {confidence}%",
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 10))

    # Risk Analysis
    content.append(
        Paragraph(
            "<b>Academic Risk Analysis</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            f"Risk Score : {risk_score}/100",
            styles["BodyText"]
        )
    )

    content.append(
        Paragraph(
            f"Risk Level : {risk_level}",
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 10))

    # Strong Areas
    content.append(
        Paragraph(
            "<b>Academic Strengths</b>",
            styles["Heading2"]
        )
    )

    for area in strong_areas:
        content.append(
            Paragraph(
                f"• {area}",
                styles["BodyText"]
            )
        )

    content.append(Spacer(1, 10))

    # Weak Areas
    content.append(
        Paragraph(
            "<b>Areas Requiring Improvement</b>",
            styles["Heading2"]
        )
    )

    for area in weak_areas:
        content.append(
            Paragraph(
                f"• {area}",
                styles["BodyText"]
            )
        )

    content.append(Spacer(1, 10))

    # Suggestions
    content.append(
        Paragraph(
            "<b>Improvement Recommendations</b>",
            styles["Heading2"]
        )
    )

    for suggestion in suggestions:
        content.append(
            Paragraph(
                f"• {suggestion}",
                styles["BodyText"]
            )
        )

    content.append(Spacer(1, 10))

    # Career Recommendations
    content.append(
        Paragraph(
            "<b>Career Recommendations</b>",
            styles["Heading2"]
        )
    )

    for career, score in careers:
        content.append(
            Paragraph(
                f"• {career} - Career Match Score: {score}%",
                styles["BodyText"]
            )
        )

    content.append(Spacer(1, 20))

    content.append(
        Paragraph(
            "Generated by AI-Powered Student Performance Prediction System",
            styles["Italic"]
        )
    )

    doc.build(content)

    return filename